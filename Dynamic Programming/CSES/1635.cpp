const int MOD = 1e9 + 7;
const int MXN = 1e6 + 5;
ll dp[MXN];

int rec(vector< int > &c ,ll x) {
	if(x < 0) 
		return 0;
	if(dp[x] != -1)	
		return dp[x];
	dp[x] = 0;
	for(auto &i : c) 
		dp[x] = (dp[x] + rec(c , x - i) ) % MOD;
	return dp[x]; 	
}

